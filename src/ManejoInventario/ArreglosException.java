public class ArreglosException extends Exception{

    public ArreglosException() {
    }

    public ArreglosException(String message) {
        super(message);
    }

    public ArreglosException(String message, Throwable cause) {
        super(message, cause);
    }

    public ArreglosException(Throwable cause) {
        super(cause);
    }

    public ArreglosException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
    
}
